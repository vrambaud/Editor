export enum SyncType {
    /**
     * Defines the animation to be played alone.
     */
    Animation = "Animation",
    /**
     * Defines the animation to be played with the other ones of the object.
     */
    Object = "Object",
    /**
     * Defines the animation to be played with all other animations in the scene.
     */
    Scene = "Scene",
}

